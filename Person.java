public class Person{
	String name;
	int age;

	Person(String name, int age){
		this.name = name;
		this.age = age;
	}

	public void setName(String name){
		this.name = name;	
	}

	public String getName(){
		return name;
	}

	public void setage(int age)
	{
		this.age=age;	
	}

	int getage()
	{
	return age;
	}

}